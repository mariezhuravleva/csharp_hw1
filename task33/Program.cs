/*
Задайте массив. Напишите программу, которая определяет, присутствует ли заданное число в массиве.
4; массив [6, 7, 19, 345, 3] -> нет
-3; массив [6, 7, 19, 345, 3] -> да
*/
bool IsNumberPresent(int[] array, int number)
{
    for (int i = 0; i < array.Length; i++)
    {
        if (array[i] == number)
        {
            return true;
        }
    }
    return false;
}

void PrintArray(int[] array)
{
    Console.Write("[");

    for (int i = 0; i < array.Length; i++)
    {
        Console.Write(array[i]);
        if (i < array.Length - 1)
        {
            Console.Write(", ");
        }
    }
    Console.WriteLine("]");
}

int[] array = { 1, 2, 3, 4, 5 };
int targetNumber = 3;

PrintArray(array);

if (IsNumberPresent(array, targetNumber))
{
    Console.WriteLine($"Number {targetNumber} is present in the array.");
}
else
{
    Console.WriteLine($"Number {targetNumber} is not present in the array.");
}